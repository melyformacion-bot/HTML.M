(() => {
  const likeKey = "melyfy-liked-songs";
  const footerLikeButton = document.querySelector(".player-buttons button");
  let activeTrack = 0;
  let activePlaylistMode = "all";

  function readLikes() {
    return new Set(JSON.parse(localStorage.getItem(likeKey) || "[]"));
  }

  function saveLikes(likes) {
    localStorage.setItem(likeKey, JSON.stringify([...likes]));
  }

  function getSongId(song) {
    return `${song.title}__${song.artist}`;
  }

  function toggleLike(track) {
    const song = getSong(track);
    const likes = readLikes();
    const id = getSongId(song);

    if (likes.has(id)) {
      likes.delete(id);
    } else {
      likes.add(id);
    }

    saveLikes(likes);
    if (activePlaylistMode === "liked" && playlistPanel.classList.contains("is-open")) {
      openLikedSongsList();
    } else {
      updateLikeButtons();
    }
  }

  function getAllSongTracks() {
    const tracks = [...songs.map((_, index) => index)];

    Object.values(playlistCollections).forEach((collection) => {
      if (!collection?.songs) return;

      collection.songs.forEach((track) => {
        const song = getSong(track);
        const exists = tracks.some((item) => getSongId(getSong(item)) === getSongId(song));

        if (!exists) {
          tracks.push(track);
        }
      });
    });

    return tracks;
  }

  function getLikedSongTracks() {
    const likes = readLikes();
    return getAllSongTracks().filter((track) => likes.has(getSongId(getSong(track))));
  }

  function updateLikeButtons() {
    const likes = readLikes();

    document.querySelectorAll(".song-row, .collection-song-row").forEach((row) => {
      const song = getSong(getRowSong(row));
      const button = row.querySelector(".like-song");
      const isLiked = likes.has(getSongId(song));

      button?.classList.toggle("is-liked", isLiked);
      button?.setAttribute("aria-label", isLiked ? "Quitar de Tus me gusta" : "Guardar en Tus me gusta");
      button?.setAttribute("title", isLiked ? "Quitar de Tus me gusta" : "Guardar en Tus me gusta");
    });

    if (footerLikeButton) {
      const isLiked = likes.has(getSongId(getSong(activeTrack)));
      footerLikeButton.classList.add("footer-like");
      footerLikeButton.classList.toggle("is-liked", isLiked);
      footerLikeButton.setAttribute("aria-label", isLiked ? "Quitar de Tus me gusta" : "Guardar en Tus me gusta");
      footerLikeButton.setAttribute("title", isLiked ? "Quitar de Tus me gusta" : "Guardar en Tus me gusta");
    }
  }

  function addLikeButtons(container) {
    container.querySelectorAll(".song-row, .collection-song-row").forEach((row) => {
      if (row.querySelector(".like-song")) return;

      const oldAction = row.lastElementChild;
      const button = document.createElement("button");
      button.type = "button";
      button.className = "like-song";
      button.textContent = "♥";
      button.setAttribute("aria-label", "Guardar en Tus me gusta");
      button.setAttribute("title", "Guardar en Tus me gusta");
      button.addEventListener("click", (event) => {
        event.preventDefault();
        event.stopPropagation();
        toggleLike(getRowSong(row));
      });

      oldAction?.replaceWith(button);
    });

    updateLikeButtons();
  }

  function openAllSongsList() {
    activePlaylistMode = "all";
    setActiveNav(libraryNav);
    closeNavPanels();
    videoWindow.classList.remove("is-open");
    collectionWindow.classList.remove("is-open");
    playlistTitle.textContent = "Lista de canciones";
    playlistCount.textContent = `${songs.length} canciones`;
    renderSongs();
    playlistPanel.classList.add("is-open");
    addLikeButtons(songList);
  }

  function openLikedSongsList() {
    activePlaylistMode = "liked";
    const likedTracks = getLikedSongTracks();

    setActiveNav(libraryNav);
    closeNavPanels();
    videoWindow.classList.remove("is-open");
    collectionWindow.classList.remove("is-open");
    playlistTitle.textContent = "Tus me gusta";
    playlistCount.textContent = `${likedTracks.length} canciones`;
    renderSongs(likedTracks);

    if (likedTracks.length === 0) {
      songList.innerHTML = `<p class="empty-liked">Todavia no marcaste ninguna cancion con Me gusta.</p>`;
    }

    playlistPanel.classList.add("is-open");
    addLikeButtons(songList);
  }

  const originalSelectSong = selectSong;
  selectSong = function(index, selectedRow, openVideo = true) {
    activeTrack = index;
    originalSelectSong(index, selectedRow, openVideo);
    updateLikeButtons();
  };

  const observer = new MutationObserver(() => {
    addLikeButtons(songList);
    addLikeButtons(collectionSongList);
  });

  observer.observe(songList, { childList: true });
  observer.observe(collectionSongList, { childList: true });

  document.addEventListener("click", (event) => {
    const likedCard = event.target.closest('[data-collection="liked"]');
    if (!likedCard) return;

    event.preventDefault();
    event.stopImmediatePropagation();
    openLikedSongsList();
  }, true);

  toggleList.addEventListener("click", (event) => {
    event.preventDefault();
    event.stopImmediatePropagation();
    openAllSongsList();
  }, true);

  libraryNav.addEventListener("click", (event) => {
    event.preventDefault();
    event.stopImmediatePropagation();
    openAllSongsList();
  }, true);

  document.addEventListener("keydown", (event) => {
    if (event.key !== "Enter" && event.key !== " ") return;

    const likedCard = event.target.closest('[data-collection="liked"]');
    if (!likedCard) return;

    event.preventDefault();
    event.stopImmediatePropagation();
    openLikedSongsList();
  }, true);

  footerLikeButton?.addEventListener("click", (event) => {
    event.preventDefault();
    event.stopImmediatePropagation();
    toggleLike(activeTrack);
  }, true);

  addLikeButtons(songList);
  addLikeButtons(collectionSongList);
  updateLikeButtons();
})();



